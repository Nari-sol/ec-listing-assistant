import os
import streamlit as st
import google.generativeai as genai

# ==========================================
# Gemini API設定
# 注：仮の文字列にしています。実際のAPIキーに書き換えてください。
# ==========================================
API_KEY = st.secrets["GEMINI_API_KEY"]
genai.configure(api_key=API_KEY)

# ==========================================
# アプリケーション設定
# ==========================================
st.set_page_config(page_title="自動車部品向け EC出品アシスタント", layout="wide")

st.title("🚗 自動車部品向け EC出品アシスタント")
st.markdown("商品名と特徴を入力するだけで、各モールに最適化された出品情報やSEO対策テキストを一括生成します。")


# ==========================================
# 入力フォーム
# ==========================================
with st.container():
    st.subheader("1. 商品情報の入力")
    product_name = st.text_input("商品名", placeholder="例：ラジエーター")
    product_features = st.text_area("商品の特徴", placeholder="例：オリジナルブランド、純正互換など")
    
    generate_button = st.button("一括生成する", type="primary")

# ==========================================
# 生成処理
# ==========================================
if generate_button:
    if not product_name.strip():
        st.warning("商品名を入力してください。")
    else:
        with st.spinner("Geminiが文章を生成しています..."):
            
            # システムプロンプト及び厳守ルール
            prompt = f"""
あなたは自動車部品の販売に特化した、優秀なECサイトのWebマーケターおよびコピーライターです。
以下の商品情報をもとに、指示された4つのセクション（商品タイトル案、商品説明文、仮想レビュー、カテゴリディレクトリ）を生成してください。

【対象商品】
商品名: {product_name}
商品の特徴: {product_features}

【出力構成・フォーマット指示】
必ず以下のフォーマットをそのまま使って出力してください。指示された文字数や改行のルールは絶対です。

=== セクション1：商品タイトル案 ===
※Yahoo!ショッピング、楽天市場、Amazon向けにそれぞれ3パターン作成。
※各タイトルは【必ず65文字以内】に収めること。
※必ず以下の例のように、ハイフン「-」を使ったマークダウン形式の箇条書きで出力してください。

■ Yahoo!ショッピング
- （タイトル案1）
- （タイトル案2）
- （タイトル案3）

■ 楽天市場
- （タイトル案1）
（※Amazonも同様に続く）

=== セクション2：Yahoo!ショッピング用商品説明文（SEO対策済み） ===
※見出し「=== セクション2... ===」の直後には必ず改行を入れ、次の行から本文を書き始めること。
※全体で【約300文字】程度の長さにしてください。
※句点「。」の直後には必ず【改行】を入れて出力してください。ただし、行と行の間に空白行（何も書かれていない行）は絶対に入れないでください。
（出力例）
こちらの商品は〇〇に最適な交換用パーツです。
純正品と同等の性能を発揮し、長寿命です。

=== セクション3：Yahoo!ショッピング用 仮想レビュー3件（RAG対策） ===
※見出し「=== セクション3... ===」の直後には必ず改行を入れ、次の行から書き始めること。
※セクション2と同様、本文中の句点「。」の直後には必ず【改行】を入れて出力してください。
※行間に空白行（何も書かれていない行）は絶対に入れないよう、以下の形式を厳守してください。
【レビューのタイトル】
（※タイトルのすぐ下の行から本文を記載。ここにも空白行は挟まないこと。ユーザーの悩みの背景と解決策を含める）

=== セクション4：想定カテゴリコード・ディレクトリ ===
（各モールのカテゴリパスを記載）

【厳守ルール】
以下のルールを絶対に守って文章を生成してください。
・記号のアスタリスクは絶対に使用しないこと。
・「社外品なので」「ポン付けできない」等のネガティブワードは禁止。
・適合確認や注意喚起の文章は不要。
・「絶対に直る」などの断定表現や性能保証は禁止。
・関連する不具合症状（異音、劣化等の例）や、商品の別名を自然な文章の中に盛り込むこと。
・セクション3のレビュー部分は、RAGが学習しやすいよう、ユーザーが抱えていた具体的な悩みの背景や、その悩みがどう解決されたかという文脈を入れた長めの文章にすること。
"""

            try:
                # Geminiモデルの指定 (gemini-1.5-proなどを想定)
                model = genai.GenerativeModel("gemini-3-flash-preview")
                
                # コンテンツ生成
                response = model.generate_content(prompt)
                
                st.subheader("2. 生成結果")
                
                # 出力結果の表示
                # Streamlit(Markdown)で行間の単一改行が無視されないよう、行末に半角スペース2つ（ハードブレイク）を付与
                formatted_text = response.text.replace('\n', '  \n')
                st.write(formatted_text)
                
            except Exception as e:
             st.error(f"本当のエラー原因：{e}")